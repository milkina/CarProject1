package lesson;

public class Literals {
    public static void main(String[] args) {
        System.out.println("Логический литерал: " + true);
        System.out.println("Строковый литерал: " + "строка");
        System.out.println("Символьный литерал: " + 'g');
        System.out.println("Целочисленный 2-й литерал: " + 0b0001);
        System.out.println("Целочисленный 8-й литерал: " + 0126);
        System.out.println("Целочисленный 10-й литерал: " + 45);
        System.out.println("Литерал типа float : " + 6.8f);
        System.out.println("Литерал типа double : " + 9.567);
    }
}
